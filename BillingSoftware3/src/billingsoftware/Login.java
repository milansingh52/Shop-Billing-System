    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email1 = jTextField1.getText();
        char[] pass = jPasswordField1.getPassword();
        String pass1 = String.valueOf(pass);        
        try
        {
            Connection con = DbConnection.getConnection();
            PreparedStatement ps1 = con.prepareStatement("select * from admin_tbl where email=? and password=?");
            ps1.setString(1, email1);
            ps1.setString(2, pass1);
            ResultSet rs1 = ps1.executeQuery();            
            PreparedStatement ps2 = con.prepareStatement("select * from employee_tbl where email=? and password=?");
            ps2.setString(1, email1);
            ps2.setString(2, pass1);
            ResultSet rs2 = ps2.executeQuery();
            boolean flag = false;
                if(rs1.next()){
                    new AdminDashboard().setVisible(true);
                    this.dispose();
                    flag = true;
                }
                if(rs2.next()){
                    new EmployeeBilling(rs2.getString("name")).setVisible(true);
                    this.dispose();
                    flag = true;
                }
                if(flag==false)
                {
                    JOptionPane.showMessageDialog(rootPane, "Incorrect Username or Password","Error",JOptionPane.ERROR_MESSAGE);
                }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

